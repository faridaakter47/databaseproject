  <h2>Eligibility to Donate Blood</h2>
  <h4>Basic Requirements</h4>

   	<li>Be at least 17 years old in most states.</li>
   	<li>Weight: At least 45 kg (100 Ibs) for both males and females.</li>
	<li>Well Being: Feeling well that day. Not having colds, coughs or flu in the last one week. No fever (Temperature >37.5°C) in the last 3 weeks.</li>
  <h2>What Happens to Donated Blood?</h2>

  <h4>Step 1: The Donation</h4>

	<li>Donor registers</li>
	<li>Health history and mini physical are completed</li>
	<li>About 1 pint of blood and several small test tubes are collected from each donor</li>
	<li>The bag, test tubes and the donor record are labeled with an identical bar code label to keep track of the donation</li>
	<li>The donation is stored in iced coolers until it is transported to a Red Cross center</li>

  <h4>Step 2: Processing</h4>
    <li>Donated blood is scanned into a computer database</li>
	<li>Most blood is spun in centrifuges to separate the transfusable components – red cells, platelets, and plasma</li>
	<li>The primary components like plasma, can be further manufactured into components such as cryoprecipitate</li>
	<li>Red cells are then leuko-reduced</li>
	<li>Single donor platelets are leukoreduced and bacterially tested.</li>
	<li>Test tubes are sent for testing.</li>

 <h4>Step 3: Testing</h4>
   <li>Steps 2 and 3 take place in parallel</li>
	<li>The test tubes are received in one of five Red Cross National Testing Laboratories</li>
	<li>A dozen tests are performed on each unit of donated blood – to establish the blood type and test for infectious diseases</li>
	<li>Test results are transferred electronically to the manufacturing facility within 24 hours</li>
	<li>If a test result is positive, the unit is discarded and the donor is notified. Test results are confidential and are only shared with the donor, except as may be required by law</li>

 <h4>Step 4: Storage</h4>
	<li>When test results are received, units suitable for transfusion are labeled and stored</li>
	<li>Red Cells are stored in refrigerators at 6ºC for up to 42 days</li>
	<li>Platelets are stored at room temperature in agitators for up to five days</li>
    <li>Plasma and cryo are frozen and stored in freezers for up to one year</li>
 <h4>Step 5: Distribution</h4>
	<li>Blood is available to be shipped to hospitals 24 hours a day, 7 days a week.</li>


